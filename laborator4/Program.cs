// --------------------
// 1. Присваивание одной переменной к другой
// --------------------
Console.WriteLine("1. Присваивание одной переменной к другой");

int a1 = 5;
int b1 = 6;
a1 = b1;
b1 = 7;
Console.WriteLine(a1);

// Объяснение:
// 1) Создаются две переменные типа int: a1 и b1.
// 2) В a1 записывается 5.
// 3) В b1 записывается 6.
// 4) Команда a1 = b1 копирует значение из b1 в a1.
//    Теперь в a1 лежит 6.
// 5) В b1 записывается 7.
// 6) Значение a1 уже не меняется, там остается 6.
// 7) В консоль выводится 6.
//
// Временная память (стек):
// Выделяется 2 ячейки: для a1 и для b1.
//
// Динамическая память (хип):
// Не выделяется ни одного объекта, так как int - тип-значение.
//
// Итог:
// a1 = 6
// b1 = 7
// В консоль: 6

Console.WriteLine();


// --------------------
// 2. Присваивание выражения, включающего переменную
// --------------------
Console.WriteLine("2. Присваивание выражения, включающего переменную");

int a2 = 5;
int b2 = a2 + 6;
a2 = 7;
Console.WriteLine(b2);

// Объяснение:
// 1) Создаются переменные a2 и b2 типа int.
// 2) В a2 записывается 5.
// 3) Вычисляется выражение a2 + 6, то есть 5 + 6 = 11.
// 4) В b2 записывается 11.
// 5) Потом в a2 записывается 7.
// 6) Это не влияет на b2, потому что в b2 уже записано готовое значение 11.
// 7) В консоль выводится 11.
//
// Временная память (стек):
// Выделяется 2 ячейки: a2 и b2.
//
// Динамическая память (хип):
// Объекты не создаются.
//
// Итог:
// a2 = 7
// b2 = 11
// В консоль: 11

Console.WriteLine();


// --------------------
// 3. Присваивание ссылочной переменной
// --------------------
Console.WriteLine("3. Присваивание ссылочной переменной");

string a3 = "1";
string b3 = a3;
a3 = "2";
Console.WriteLine(a3);
Console.WriteLine(b3);

// Объяснение:
// 1) Создается переменная a3 типа string.
// 2) Строковый объект "1" существует в памяти, a3 хранит ссылку на него.
// 3) Создается переменная b3.
// 4) Команда b3 = a3 копирует ссылку.
//    Теперь и a3, и b3 ссылаются на строку "1".
// 5) Команда a3 = "2" меняет ссылку в переменной a3.
//    Теперь a3 ссылается на строку "2".
// 6) b3 по-прежнему ссылается на строку "1".
// 7) В консоль выводится сначала 2, потом 1.
//
// Временная память (стек):
// Выделяется 2 ячейки: a3 и b3.
// В них хранятся не сами строки, а ссылки.
//
// Динамическая память (хип):
// Логически выделяются 2 строковых объекта: "1" и "2".
//
// Итог:
// a3 -> "2"
// b3 -> "1"
// В консоль:
// 2
// 1

Console.WriteLine();


