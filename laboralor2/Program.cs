// После удаления папок bin и obj команда dotnet run --no-build
// выдает ошибку, потому что она НЕ компилирует проект.
// Если готовый исполняемый файл отсутствует, запуск невозможен.
//
// Команда dotnet build заново создает артефакты компиляции:
// - папку obj (временные файлы сборки)
// - папку bin (готовые результаты сборки, включая .exe / .dll)
//
// bin - обычно содержит итоговые файлы, которые можно запускать.
// obj - содержит промежуточные служебные файлы компиляции.
// ------------------------------------------------------------


// Основная программа начинается здесь.
Console.WriteLine("Hello, Jerry");

// Вызываем функцию, которая печатает A B C, три раза
PrintABC();
Thread.Sleep(500);

PrintABC();
Thread.Sleep(500);

PrintABC();
Thread.Sleep(500);

// Вызываем функцию A несколько раз
FunctionA();
Console.WriteLine();

FunctionA();
Console.WriteLine();

FunctionA();
Console.WriteLine();


// ------------------------------------------------------------
// Что будет, если функцию нигде не вызвать?
// Ответ:
// Если функция определена, но нигде не вызвана,
// она НЕ выполнится сама по себе.
//
// Выполнение функции происходит только тогда,
// когда программа доходит до ее вызова.
//
// Если вызова нет, функция просто существует в коде,
// но не выполняется.
// ------------------------------------------------------------


// ------------------------------------------------------------
// Важен ли порядок определения функций?
// Ответ:
// Для C# в одном файле порядок определения функций не важен.
// Можно вызвать функцию, даже если она определена ниже по коду.
//
// То есть функция, определенная раньше в файле,
// может ссылаться на функцию, определенную позже.
// Ниже это показано на примере: FunctionA вызывает FunctionB и FunctionC,
// хотя они могут быть объявлены позже.
// ------------------------------------------------------------


// ------------------------------------------------------------
// Что такое "определение" функции?
// Ответ:
// Определение функции - это описание самой функции:
// ее имя, параметры (если есть) и код, который она выполняет.
//
// Пример определения:
// void PrintABC()
// {
//     Console.WriteLine("A");
//     Console.WriteLine("B");
//     Console.WriteLine("C");
// }
//
// А вот вызов функции - это использование ее имени:
// PrintABC();
// ------------------------------------------------------------


// Функция печатает A B C
void PrintABC()
{
    Console.WriteLine("A");
    Console.WriteLine("B");
    Console.WriteLine("C");
}


// Функция A вызывает B и C
void FunctionA()
{
    Console.WriteLine("Начало функции A");
    FunctionB();
    FunctionC();
    Console.WriteLine("Конец функции A");
}


// Функция B
void FunctionB()
{
    Console.WriteLine("Это функция B");
}


// Функция C
void FunctionC()
{
    Console.WriteLine("Это функция C");
}